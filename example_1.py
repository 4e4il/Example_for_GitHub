from turtle import*  # Імпортуємо модуль для роботи з графікою "turtle".
from random import*  # Імпортуємо модуль "random" для генерації випадкових чисел.

# Налаштовуємо екран
screen = Screen()  # Створюємо екран для гри.
screen.setup(width=500, height=500)  # Встановлюємо розмір вікна 500x500 пікселів.

# Налаштовуємо текст заголовку
label = Turtle()  # Створюємо черепашку для напису.
label.penup()  # Піднімаємо перо, щоб не залишати слідів.
label.hideturtle()  # Ховаємо черепашку.
label.goto(0, 170)  # Переміщуємо черепашку до координат (0, 170).
label.color("red")  # Встановлюємо червоний колір пера.
label.write("turtle racing", font=("Arial", 24), align="center")  # Пишемо заголовок.

# Малювання треку
speed(150)  # Встановлюємо високу швидкість малювання.
penup()  # Піднімаємо перо.
goto(-140, 140)  # Переходимо до верхнього лівого кута.

# Малюємо трек із номерами доріжок
for i in range(15):  # Малюємо 15 ліній для треку.
    write(i, align='center')  # Пишемо номер доріжки.
    right(90)  # Повертаємося вниз.
    forward(10)  # Малюємо короткий відрізок.
    pendown()  # Опускаємо перо для пунктирної лінії.
    for i in range(8):  # Малюємо пунктир.
        forward(10)  # Малюємо відрізок.
        penup()
        forward(10)  # Створюємо пробіл між пунктиром.
        pendown()
    penup()  # Піднімаємо перо.
    backward(170)  # Повертаємося назад до початку доріжки.
    left(90)  # Повертаємося вліво.
    forward(20)  # Зсуваємося до наступної доріжки.

# Малюємо фінішну лінію
write("finish")  # Пишемо текст "finish" у верхньому кінці фінішної лінії.
pendown()  # Опускаємо перо.
width(3)  # Встановлюємо товщину лінії 3 пікселі.
color("red")  # Встановлюємо червоний колір лінії.
right(90)  # Повертаємося вниз.
forward(160)  # Малюємо фінішну лінію.
hideturtle()  # Ховаємо черепашку.

# Створюємо черепашок для перегонів
a = Turtle()  # Створюємо черепашку "a".
a.color('red')  # Задаємо їй червоний колір.
a.shape('turtle')  # Встановлюємо форму черепашки.
a.penup()  # Піднімаємо перо.
a.goto(-160, 100)  # Розташовуємо черепашку на старті.

b = Turtle()  # Створюємо черепашку "b".
b.color('blue')  # Задаємо їй синій колір.
b.shape('turtle')  # Встановлюємо форму черепашки.
b.penup()
b.goto(-160, 70)  # Розташовуємо черепашку на старті.

c = Turtle()  # Створюємо черепашку "c".
c.color('green')  # Задаємо їй зелений колір.
c.shape('turtle')
c.penup()
c.goto(-160, 40)  # Розташовуємо черепашку на старті.

d = Turtle()  # Створюємо черепашку "d".
d.color('yellow')  # Задаємо їй жовтий колір.
d.shape('turtle')
d.penup()
d.goto(-160, 10)  # Розташовуємо черепашку на старті.

# Гонка черепашок
for turn in range(100):  # Черепашки роблять 100 кроків.
    a.forward(randint(1, 5))  # Черепашка "a" рухається на випадкову відстань.
    b.forward(randint(1, 5))  # Черепашка "b" рухається на випадкову відстань.
    c.forward(randint(1, 5))  # Черепашка "c" рухається на випадкову відстань.
    d.forward(randint(1, 5))  # Черепашка "d" рухається на випадкову відстань.

# Знаходимо переможця
max_x = a.xcor()  # Припускаємо, що черепашка "a" лідер.
max_l = a  # Зберігаємо посилання на черепашку.
if b.xcor() > max_x:  # Якщо "b" попереду:
    max_x = b.xcor()  # Оновлюємо максимальну координату.
    max_l = b  # Оновлюємо лідера.
if c.xcor() > max_x:  # Якщо "c" попереду:
    max_x = c.xcor()
    max_l = c
if d.xcor() > max_x:  # Якщо "d" попереду:
    max_x = d.xcor()
    max_l = d

# Виводимо результат
label.goto(0, -170)  # Переміщуємо напис вниз.
max_l.goto(0, -175)  # Переміщуємо переможця до тексту.
label.write(f" winner: {int(max_x)}", font=("Arial", 16, "normal"), align="center")  # Виводимо текст з координатою переможця.

# Не закриваємо вікно автоматично
screen.mainloop()  # Підтримуємо вікно відкритим.
